// ── Types ──
export interface ActionDef {
  key: string;
  label: string;
  icon?: string;
  variant?: "primary" | "secondary" | "danger" | "ghost";
  disabled?: boolean;
  loading?: boolean;
  shortcut?: string;
  visible?: boolean | (() => boolean);
  action: () => void | Promise<void>;
}

const props = withDefaults(
  defineProps<{
    actions: ActionDef[];
    align?: "left" | "right";
    size?: "sm" | "md";
  }>(),
  {
    align: "left",
    size: "md",
  },
);

// ── Helpers ──
function isVisible(a: ActionDef): boolean {
  if (typeof a.visible === "function") return a.visible();
  return a.visible !== false;
}

const visibleActions = computed(() => props.actions.filter(isVisible));

// ── Button variant → CSS class ──
function btnClass(variant: ActionDef["variant"]): string {
  const map: Record<string, string> = {
    primary: "btn btn-primary",
    secondary: "btn btn-secondary",
    danger: "btn btn-danger",
    ghost: "btn btn-ghost",
  };
  const base = map[variant ?? "secondary"];
  if (props.size === "sm") return `${base} btn-sm`;
  return base;
}

